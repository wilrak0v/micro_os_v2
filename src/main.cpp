void setup() {
    lcd.init();
    lcd.setRotation(3);
    lcd.clear(TFT_BLACK);
}

void loop() {
    old_box_x = box_x;
    old_box_y = box_y;

    box_x += vel_x;
    box_y += vel_y;

    if (box_x <= 0 || box_x + box_size >= 320) {
        vel_x = -vel_x;
    }
    if (box_y <= 0 || box_y + box_size >= 240) {
        vel_y = -vel_y;
    }

    renderer.fillRect(old_box_x, old_box_y, box_size, box_size, TFT_BLACK);
    renderer.fillRect(box_x, box_y, box_size, box_size, TFT_GREEN);

    renderer.drawLine(200, 200, 260, 220, TFT_YELLOW);

    renderer.drawText("micro_os", 50, 10, 3, TFT_CYAN);

    renderer.fillRoundRect(250, 120, 50, 50, 13, TFT_BLUEVIOLET);

    renderer.drawRect(160, 120, 20, 20, TFT_RED);

    renderer.drawScaledBitmap(20, 40, 12, 16, 2, 0x6C7F, mario);

    renderer.render();

    delay(16); // ~60 FPS
}